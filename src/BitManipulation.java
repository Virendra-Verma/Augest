public class BitManipulation {
    public static void main(String[] args) {
        System.out.println("The AND operator of 5 and 6 : "+ (5&6));
        System.out.println("The OR operator of 5 and 6 : "+ (5|6));
        System.out.println("The XOR operator of 5 and 6 : "+ (5^6));
        System.out.println("The NOT operator of 5  : "+ ~5);

    }
}
